def add(*val):
    ans=0
    for i in val:
        ans+=i
    print(ans)

def sub(*val):
    ans=0
    for i in val:
        ans-=i
    print(ans)
        
def mul(*val):
    ans=1
    for i in val:
        ans*=i
    print(ans)

